public class Verzweigung_Switch
{
    public static void main(String[]args)
    {
        switch (5)
        {
            case 0:

                System.out.println("Zahl ist 0");
                break;
            case 3:
                System.out.println("Zahl ist 3");
                break;

            case 5:
                System.out.println("Zahl ist 5");
                break;

        }
    }
}
