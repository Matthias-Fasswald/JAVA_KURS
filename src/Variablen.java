public class Variablen {

    public static void main(String[]args)
    {
        int varZahl1 = 3;
        long varZahl2 = 300;
        double varZahl3 = 322.23;
        float varZahl4 = 23.45f;
        boolean userKlick = true;   //entweder ture oder false
        char varZeichen = '!';



        System.out.println("int: " + varZahl1);
        System.out.println("long: " + varZahl2);
        System.out.println("double: " + varZahl3);
        System.out.println("float: " + varZahl4);
        System.out.println("boolean: " + userKlick);
        System.out.println("char: " + varZeichen);
    }
}
