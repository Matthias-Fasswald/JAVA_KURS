public class int_float_double {

    public static void main(String[]args)
    {
        int varZahl1 = 10 / 3;
        float varZahl2 = 10f / 3f; // wichtig ist hierbei das "f" für float
        double varZahl3 = 10d / 3d; // hierbei das "d" für double

        System.out.println("int " + varZahl1);
        System.out.println("float " + varZahl2);
        System.out.println("double " + varZahl3);
    }
}
