public class Verzweigung_If_Else
{
    public static void main(String[] args)
    {
        int i = 1;
        if(i == 1)
            System.out.println("Zahl in i ist: "+i);
        else if(i >= 1)
            System.out.println("Zahl in i ist: "+i);
        else if(i <= 1)
            System.out.println("Zahl in i ist: "+i);
        else
            System.out.println("Zahl unbekannt!");
    }
}
